import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import YAML from "yaml";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, "..", "..", "..");
const sourcePath = path.join(repoRoot, "apps", "runner", "src", "agents", "agents.yaml");
const outputPath = path.join(repoRoot, "packages", "shared", "src", "generated-agent-catalog.ts");

const raw = await fs.readFile(sourcePath, "utf8");
const catalog = YAML.parse(raw);

const output = `// Generated from apps/runner/src/agents/agents.yaml by apps/runner/scripts/generate-agent-catalog.mjs
// Do not edit this file by hand.
import type { AgentConfig } from "./types.js";

export const RAW_AGENT_CATALOG = ${JSON.stringify(catalog, null, 2)} satisfies AgentConfig[];
`;

await fs.writeFile(outputPath, output);
console.log(`Wrote ${outputPath}`);
